/**
 * lib/variants.ts — каталог сайтов-вариантов под один код.
 *
 * Каждый домен имеет собственный уникальный title/H1/intro/canonical,
 * чтобы Яндекс не склеил их в дубли и выдал все 6 сайтов по разным запросам.
 *
 * Активный вариант выбирается через env `NEXT_PUBLIC_SITE_VARIANT`
 * (build-time), default = 'main'.
 *
 * Добавление нового варианта:
 *   1) Положить новый ключ в VARIANTS.
 *   2) Перечислить домен в massive `hosts`.
 *   3) Пересобрать: `SITE_VARIANT=<key> npm run build:export`.
 *   4) Задать DNS A-запись на IP VPS, добавить server{} в nginx.
 */

export type SiteVariant = {
  /** Основной домен без протокола. */
  host: string;
  /** Альтернативы (www, кириллическое написание), 301-редиректят на host. */
  aliases: string[];
  /** <title> всей страницы. */
  title: string;
  /** H1 в Hero. */
  h1: string;
  /** Акцентная подсветка в H1 (выделяется gradient’ом). */
  h1Accent: string;
  /** Подзаголовок Hero — прямая боль/триггер. */
  heroSubtitle: string;
  /** 3-4 абзаца intro после героя (для SEO-уникальности). */
  intro: string[];
  /** <meta name="description">. */
  description: string;
  /** Open Graph/Twitter description (коротко). */
  ogDescription: string;
  /** Геофокус для Яндекс.Вебмастера. */
  region: 'Россия' | 'Санкт-Петербург' | 'Ленинградская область' | 'СЗФО';
  /** ISO 3166-2 код региона для geo.region meta (RU-SPE, RU-LEN, RU). */
  geoRegion?: 'RU' | 'RU-SPE' | 'RU-LEN';
  /** Метка в Hero (например «По всей России» или «в Санкт-Петербурге»). */
  regionBadge: string;
  /** Яндекс.Метрика: либо общий счётчик, либо per-domain (заполнить после регистрации). */
  metrikaId: string;
  /**
   * Яндекс.Вебмастер: per-domain токен подтверждения владения (meta-тег).
   * ВАЖНО: реальный токен выдаёт панель webmaster.yandex.ru →
   * «Добавить сайт» → «Мета-тег». Мы НЕ можем придумать свой.
   * Пусто — meta-тег не рендерится (безопасно). Заполнять после деплоя.
   */
  yandexVerification?: string;
  /** Дополнительный keyword-focus для ключевых слов в layout. */
  keywords: string[];
  /** breadcrumbs / JSON-LD Organization url — всегда https://host/. */
  canonicalBase: string;
  /** Если true — домен считается техническим зеркалом (301 на gisprof.ru), HTML не рендерится. */
  mirrorOnly?: boolean;
  /** Редирект-цель, если mirrorOnly. */
  mirrorTarget?: string;
};

